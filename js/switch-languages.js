// Process translation

jQuery(document).ready(function($) {
    function changeLang(langType) {
        $('.lang').each(function(index, item) {
            if (this.tagName === 'INPUT') {
                this.placeholder = arrLang[langType][$(this).attr('key')];
            }
            $(this).text(arrLang[langType][$(this).attr('key')]);
        })
    }

    $(function() {
        $('.translate').click(function() {
            let lang = $(this).attr('id');
            if ($(this).attr('key') === 'en') {
                $('.ruLang').addClass('noActive');
                $('.enLang').removeClass('noActive');
            }

            if ($(this).attr('key') === 'ru') {
                $('.enLang').addClass('noActive');
                $('.ruLang').removeClass('noActive');
            }

            $('.currentLang').html(lang);
            localStorage.setItem('selectedLanguage', lang);
            changeLang(lang);
        });
    });


    let selectedLang = localStorage.getItem('selectedLanguage');

    if (selectedLang) {
        $('.currentLang').html(selectedLang);
        changeLang(selectedLang);
    }
});